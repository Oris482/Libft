char	*ft_strrchr(const char *s, int c)
{
	int	cnt;

	cnt = 0;
	while (*s++ != '\0')
		cnt++;
	while (cnt-- > 0)
	{
		s--;
		if (*s == c)
			return (s);
	}
	return (0);
}
