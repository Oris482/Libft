#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	idx;

	idx = 0;
	while ((*s1++ != '\0' && *s2++ != '\0') && idx < n)
		idx++;
	return (*s1 - *s2);
}
